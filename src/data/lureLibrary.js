export function flattenLures() {
  const rows = [];

  LURE_GROUPS.forEach((group) => {
    Object.entries(group.categories).forEach(([category, lures]) => {
      lures.forEach((type) => {
        rows.push({
          group: group.group,
          category,
          type,
          search: `${group.group} ${category} ${type}`.toLowerCase()
        });
      });
    });
  });

  return rows;
}